Command* MenuState::InputHandler(char input) {
    switch (input) {
        case 's': return new ChangeStateCommand(new PlayingState(GameManager::GetInstance()->GetSnake()));
        case 'q': return new QuitCommand();
        default:
            return nullptr;
    }
}
